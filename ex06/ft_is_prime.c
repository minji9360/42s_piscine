int		ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb <= 1)
		return (0);
	while (1)
		if (i <= nb - 1)
		{
			if (nb % i == 0)
				return (0);
			else
				i++;
		}
		else
			return (1);
}
